# 城市
D1 = {
    1: "上海",
    2: "西安",
    3: "昆明",
    4: "深圳",
    5: "天津",
    6: "郑州",
}

# 从i到j的天数
T = [[0, 3, 6, 4, 3, 2],
     [3, 0, 4, 4, 2, 1],
     [6, 4, 0, 2, 6, 5],
     [4, 4, 2, 0, 5, 4],
     [3, 2, 6, 5, 0, 1],
     [2, 1, 5, 4, 1, 0]]

# 12t卡车从i到j的成本
W12 = [[0, 13500, 26500, 16500, 13500, 8500],
       [13500, 0, 18500, 20500, 10500, 3500],
       [26500, 18500, 0, 7500, 31500, 24500],
       [16500, 20500, 7500, 0, 25500, 17500],
       [13500, 10500, 31500, 25500, 0, 5500],
       [8500, 3500, 24500, 17500, 5500, 0]]

# 5t卡车从i到j的成本
W5 = [[0, 11000, 24000, 14000, 11000, 6000],
      [11000, 0, 16000, 18000, 8000, 1000],
      [24000, 16000, 0, 5000, 29000, 22000],
      [14000, 18000, 5000, 0, 23000, 15000],
      [11000, 8000, 29000, 23000, 0, 3000],
      [6000, 1000, 22000, 15000, 3000, 0]]

# 计算从i到j的总租金
length = 6
r_12 = 5000
r_5 = 3000
for i in range(0, length):
    for j in range(0, length):
        W12[i][j] += T[i][j] * r_12
        W5[i][j] += T[i][j] * r_5
print(W12)
print(W5)

print("对于12t卡车来说：")
for i in [0, 1, 5]:
    for j in [2, 3, 4]:
        if i == j:
            continue
        for k in range(0, length):
            if i == k or j == k:
                continue
            if W12[i][j] > W12[i][k] + W12[j][k]:
                c1 = D1[i+1]
                c2 = D1[j+1]
                c3 = D1[k+1]
                W12[i][j] = W12[i][k] + W12[j][k]
                W12[j][i] = W12[i][k] + W12[j][k]
                w = W12[i][k] + W12[j][k]
                print(f"从{c1}到{c2}时，经过{c3}时更优，此时成本为{w}")
print(W12)
